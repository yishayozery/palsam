# Apply merge map
merged_companies = []
item_type_set = set()

for c in data['companies']:
    merged_locs = []
    for loc in c['locations']:
        merged_items = {}
        for item in loc['items']:
            canonical = MERGE.get(item['name'], item['name'])
            if canonical in merged_items:
                merged_items[canonical]['qty'] += item['qty']
            else:
                merged_items[canonical] = {
                    'name': canonical,
                    'qty': item['qty'],
                    'association': item['association']
                }
            item_type_set.add(canonical)
        merged_locs.append({
            'name': loc['name'],
            'items': list(merged_items.values())
        })
    merged_companies.append({
        'sheet': c['sheet'],
        'companyName': c['companyName'],
        'locations': merged_locs
    })

output = {
    'companies': merged_companies,
    'vehicles': data['vehicles']
}

# Stats
total = sum(len(loc['items']) for c in merged_companies for loc in c['locations'])
print(f'Before merge: 394 item rows, 371 unique names')
print(f'After merge: {total} item rows, {len(item_type_set)} unique ItemTypes')
print()
for c in merged_companies:
    items_count = sum(len(loc['items']) for loc in c['locations'])
    locs = ', '.join(f'{l["name"]}({len(l["items"])})' for l in c["locations"])
    print(f'  {c["companyName"]}: {items_count} items — {locs}')

with open('4/equipment-merged.json', 'w', encoding='utf-8') as f:
    json.dump(output, f, ensure_ascii=False, indent=2)
print(f'\nSaved to 4/equipment-merged.json')
